x = 11 #x가 숫자 11로 초기화 되었습니다.
y = 22 #y가 숫자 22로 초기화 되었습니다.

print("변경 전: x = " + str(x) + ", y = " + str(y)) #"+"를 사용하기 위해 다른 자료들의 타입과 같이 x와 y를 문자열로 캐스팅 하였습니다.

z = x #z값은 현재 x값인 11이 되었습니다.
x = y #x값은 현재 y값인 22가 되었습니다.
y = z #y값은 현재 z값인 11이 되었습니다.

print("변경 전: x = " + str(x) + ", y = " + str(y)) #"+"를 사용하기 위해 다른 자료들의 타입과 같이 x와 y를 문자열로 캐스팅 하였습니다.
